# Sample tuple for demonstration
sample_tuple = (1, 2, 3, 4, 5, 2, 3)

# Commonly Used Tuple Methods
print("Count occurrences of 2:", sample_tuple.count(2))  # Counts occurrences of a value
print("Index of first occurrence of 3:", sample_tuple.index(3))  # Finds the first index of a value
