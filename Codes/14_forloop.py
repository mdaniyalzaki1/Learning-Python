"""
Python For Loop - A Detailed Explanation

This script covers:
- Syntax of for loops
- Iterating over sequences
- Using range() with for loops
"""

# For Loop Syntax
for i in range(5):  # Loops from 0 to 4
    print("Iteration:", i)

# Iterating Over a List
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("Fruit:", fruit)

# Using range with start, stop, and step
for num in range(1, 10, 2):  # Loops from 1 to 9 with step 2
    print("Number:", num)
